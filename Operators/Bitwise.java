package Operators;

public class Bitwise {
    
    public static void main(String[] args) {
        System.out.println(10 & 20);          //AND
        System.out.println(10 | 20);          //OR
        System.out.println(10 ^ -20);          //XOR
        System.out.println(~42);             //COMPLIMENT
        System.out.println(34 >> 2);   //Right Shift
        System.out.println(34 << 1);   //Left Shift
        System.out.println(-4 >> 1);   //Right Shift
        System.out.println(-4 << 1);   //Left Shift
    }
}
